    @Override
    public void stateChanged(ChangeEvent e) {
        Object source=e.getSource();
        if (source==alpha){
            alphaSlider.setValue((int)alpha.getFloatValue());
        }else{
            alpha.setFloatValue(alphaSlider.getValue()/255f);
        }
        repaint();
    }
    
