    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
// TODO add your handling code here:
        if (text.getText().equals(leaf.getName()) && locked.isSelected()==leaf.isLocked() && alpha.getFloatValue()==leaf.getAlpha()){
            this.setVisible(false);
            return;
        }
        if (!text.getText().equals(leaf.getName())){
            SortedMap map=leaf.getPage().getNameTable();
            if (map.containsKey(text.getText())){
                JOptionPane.showMessageDialog(this,"入力されたオブジェクトの名称は、他で使用されています.");
                text.setText(leaf.getName());
                text.requestFocus();
                return;
            }
        }
 
        //ObjectOptionEdit anEdit=new ObjectOptionEdit(leaf.getDocument().getViewer(),leaf,visible.isSelected(),locked.isSelected(),text.getText());
        Number al=(Number)alpha.getValue();
        ImageObjectOptionEdit anEdit=new ImageObjectOptionEdit(leaf.getDocument().getViewer(),leaf,visible.isSelected(),
                locked.isSelected(),al.floatValue(),text.getText());
        leaf.getDocument().fireUndoEvent(anEdit);
        leaf.getDocument().getViewer().repaint();
        this.getParent().repaint();
        this.setVisible(false);
        
    }//GEN-LAST:event_okActionPerformed
    
    
